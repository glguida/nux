static struct fbdesc *fbdesc;
static lock_t fblock;

int
framebuffer_init (struct fbdesc *desc)
{
  if (desc->type == FB_INVALID)
    return 0;

  assert (desc->type == FB_RGB);
  fbdesc = desc;
  memset ((void *) (uintptr_t) fbdesc->addr, 0, fbdesc->size);
  framebuffer_reset ();
  return 1;
}

